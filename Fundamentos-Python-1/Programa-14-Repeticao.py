# Pseudocódigo e introdução aos loops
# Pseudocódigo
# Início
#   Se a variável 'a' for igual a 5, então
#       Imprimir "A variável 'a' é igual a 5."
#   Senão
#       Imprimir "A variável 'a' não é igual a 5."
# Fim

# Estruturas de repetição (loops)
# Loop while
contador = 0
while contador < 5:
    print("Contador:", contador)
    contador += 1

# Loop for
for i in range(5):
    print("Iteração:", i)