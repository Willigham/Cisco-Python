# Um literal são dados cujos valores são determinados pelo próprio literal.
print("2")
print(2)

# numeros octal
print(0o123)

# numeros hexadecimal
print(0x123)

# numeros float
print(3.14)

# String
print("Olá, Mundo!")

# valores Booleanos
print(True)
print(False)
print()
print(True > False)
print(True < False)